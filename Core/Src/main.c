float IIR_FILTER(float invar, int initval, int setic)
{
    float sumnum=0.0, sumden=0.0;  int i=0;
    static float states[3] = {0.0,0.0,0.0};
    static float znum[3] = {
        0.0,
        8.3840825e-04,
        9.1165698e-04
    };
    static float zden[3] = {
        -.77776768,
        2.5285206,
        -2.7490028
    };
    if (setic==1){
        
        //for (i=0;i<3;i++) states[i] = [i] = [i]*initval;
        return initval;
    }
    else{
        sumnum = sumden = 0.0;
        for (i=0;i<3;i++){
            sumden += states[i]*zden[i];
            sumnum += states[i]*znum[i];
            if (i<2) states[i] = states[i+1];
        }
        states[2] = invar-sumden;
        return sumnum;
    }
}































